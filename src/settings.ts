import { App, PluginSettingTab, Setting } from "obsidian";
import BookishPlugin from "./main";

export interface BookishPluginSettings {
	mySetting: string;
}

export const DEFAULT_SETTINGS: BookishPluginSettings = {
	mySetting: "default",
};

export class BookishSettingTab extends PluginSettingTab {
	plugin: BookishPlugin;

	constructor(app: App, plugin: BookishPlugin) {
		// eslint-disable-next-line @typescript-eslint/no-unsafe-argument
		super(app, plugin);

		// eslint-disable-next-line @typescript-eslint/no-unsafe-assignment
		this.plugin = plugin;
	}

	display(): void {
		const { containerEl } = this;

		containerEl.empty();

		new Setting(containerEl)
			.setName("Settings #1")
			.setDesc("It's a secret")
			.addText((text) =>
				text
					.setPlaceholder("Enter your secret")
					.setValue(this.plugin.settings.mySetting)
					.onChange(async (value) => {
						this.plugin.settings.mySetting = value;
						await this.plugin.saveSettings();
					}),
			);
	}
}
